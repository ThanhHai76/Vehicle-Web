      }
    },

    async getListModel (code) {
      try {
        this.selectData.codeTransport = code
        this.modelOptions = []
        this.selectData.model = null
        const { data } = await TransportService.getListTransport({
          codeParent: code ? code : ''
        })
        const modelOptions = data.transportListRes.map((e) => {
          return { value: e.code, text: e.name }
        })
        if (modelOptions.length > 0) {
          modelOptions.shift()
          this.modelOptions = [{ value: null, text: 'Chọn Model' }, ...modelOptions]
        } else {
          this.modelOptions = [{ value: null, text: 'Chọn Model' }]
        }
      } catch (error) {
        console.log(error)
      }
    },

    changeModel (code) {
      this.selectData.codeTransport = code
    },

    async getListCity () {
      try {
        const { data } = await ConfigService.getCityList({})
        const provinceOptions = data.map((e) => {
          return {
            value: e.code,
            text: e.name
          }
        })
        this.provinceOptions = [{ value: null, text: 'Chọn tỉnh thành' }, ...provinceOptions]
      } catch (error) {
        console.log(error)
      }
    },

    async getPriceYearRange () {
      try {
        const [configPrice, configYear] = await
          Promise.all([
            ConfigService.getPriceYearRange({code: 'RANGE_PRICE'}),
            ConfigService.getPriceYearRange({code: 'RANGE_YEAR'})
          ])
        this.configRange = {
          price: configPrice.data,
          year: configYear.data
        }
        localStorage.setItem('PriceYearRange', JSON.stringify(this.configRange))
      } catch (error) {
        console.log(error)
      }
    },

    async submitSearch() {
      try {
        const { price, year } = this.configRange

        this.selectData.minPrice = document.querySelector('.irs-from') ? Number(document.querySelector('.irs-from').textContent.replace(' ', '')) : price.minValue
        this.selectData.maxPrice = document.querySelector('.irs-to') ? Number(document.querySelector('.irs-to').textContent.replace(' ', '')) : price.maxValue
        
        this.selectData.minManufactureYear = document.querySelector('.price_range.year_select .irs-from') ? Number(document.querySelector('.price_range.year_select .irs-from').textContent.replace(' ', '')) : year.minValue
        this.selectData.maxManufactureYear = document.querySelector('.price_range.year_select .irs-to') ? Number(document.querySelector('.price_range.year_select .irs-to').textContent.replace(' ', '')) : year.maxValue

        const response = await VehicleService.getVehicleList({
          codeTransport: this.selectData.codeTransport,
          codeCity: this.selectData.codeCity,
          minPrice: this.selectData.minPrice * 1000000,
          maxPrice: this.selectData.maxPrice * 1000000,
          minManufactureYear: this.selectData.minManufactureYear,
          maxManufactureYear: this.selectData.maxManufactureYear,
          status: this.selectData.status,
          limit: this.search.limit,
          page: this.search.page
        })
        if (response.code === 1000) {
          this.dataVehicleList = response.data.vehicleList
          this.search.total = response.totalPage.total
          this.totalPage = Math.floor(this.search.total / this.search.limit) + 1
        } else {
          this.dataVehicleList = []
        }
      } catch (error) {
        console.log(error)
      }
    },

    async getListFavorite () {
      try {
        const response = await VehicleService.getVehicleList({
          codeTransport: 'transport_car',
          isFavorites: true,
          limit: 20,
          page: 1
        })
        this.dataVehicleFavorite = response.data.vehicleList
        this.searchFavorite.total = response.totalPage.total
      } catch (error) {
        console.log(error)
      }
    },

    clearSearch () {
      this.selectData = {
        transport: null,
        company: null,
        series: null,
        model: null,
        codeCity: null,
        status: null,
        design: null,
        fuel: null,
        minPrice: null,
        maxPrice: null,
        minManufactureYear: null,
        maxManufactureYear: null
      }
      this.submitSearch()
    },

    changePrice () {
      const stylePrice = window.getComputedStyle(document.querySelector('.irs-bar')).width
      console.log(stylePrice)
    },

    changePage (page) {
      this.search.page = page
    }
  }
}
